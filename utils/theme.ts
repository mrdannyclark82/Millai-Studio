export function applyTheme(theme: ThemeType) {
  const root = document.documentElement;
  const colors = THEMES[theme];
  
  Object.entries(colors).forEach(([shade, value]) => {
    root.style.setProperty(`--milla-${shade}`, value);
  });
}
